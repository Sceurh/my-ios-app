// Конвертер кода страны по координатам (упрощенный)
export const getCountryCodeFromCoordinates = async (
  lat: number,
  lon: number,
): Promise<CountryCode> => {
  try {
    // В реальном приложении используй API Reverse Geocoding
    // Например: Nominatim (OpenStreetMap), Google Maps Geocoding API

    // Заглушка: определяем по приблизительным координатам
    if (lat > 40 && lat < 80 && lon > 20 && lon < 180) {
      return 'RU'; // Россия и СНГ
    } else if (lat > 24 && lat < 50 && lon > -130 && lon < -60) {
      return 'US'; // США
    } else if (lat > 35 && lat < 70 && lon > -10 && lon < 40) {
      // Для Европы выбираем Германию как дефолт
      return 'DE'; // ← ПРАВИЛЬНО: 'DE' есть в CountryCode
    } else if (lat > 48 && lat < 60 && lon > -10 && lon < 2) {
      return 'UK'; // Великобритания
    } else if (lat > 41 && lat < 52 && lon > -5 && lon < 10) {
      return 'FR'; // Франция
    }

    return 'default';
  } catch {
    return 'default';
  }
};
